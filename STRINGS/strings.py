# format() function ====> used for string formatting 
# used to insert variable values in a string 
fruit1 = "mango"
fruit2 = "apple"
string = "I have fruits - {fi} and {f2}".format(fi = fruit1, f2 = fruit2)  # f1, and f2 are placeholder values 
print(string)

student_name = "Rimmi"
student_marks = 98
str3 = "The student name is {s}, and marks are {m}".format(s = student_name, m = student_marks)
print(str3)

